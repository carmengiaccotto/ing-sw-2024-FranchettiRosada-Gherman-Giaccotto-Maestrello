package it.polimi.ingsw.Model.Enumerations;

/**
 * This enumeration represents the possible colors of a card in the game.
 * Each card can be one of four colors: RED, GREEN, PURPLE, or BLUE.
 */
public enum CardColors {
    RED,    // Represents the color red.
    GREEN,  // Represents the color green.
    PURPLE, // Represents the color purple.
    BLUE    // Represents the color blue.
}